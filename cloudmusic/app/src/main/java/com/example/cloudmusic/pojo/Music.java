package com.example.cloudmusic.pojo;

public class Music {
    private String name;
    private String path;
    private int id;

    public String getName() {
        return name;
    }

    public String getPath() {
        return path;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public int getResId() {
        return id;
    }

    public void setResId(int resId) {
        this.id = id;
    }
}
